def state_to_int(state):
    return int("".join(str(b) for b in state[::-1]), 2)


configs = []

for size, tap in taps.items():
    period = 2**size - 1
    if size <= 8:
        l_steps = [5, period // 2]
    else:
        l_steps = [10, 250]

    for i, steps in enumerate(l_steps):
        L = LFSR(fpoly=tap, initstate="ones", conf="galois")
        L.runKCycle(steps - 2)
        output_int = state_to_int(L.state)

        configs.append(
            {
                "name": f"size_{size}_case_{i + 1}",
                "g_size": size,
                "g_steps": steps,
                "g_seed": bin(output_int)[2:].rjust(64, "0"),
            }
        )

with open("tests/lfsr_counter_tb.json", "w") as f:
    json.dump(configs, f, indent=4)

print(f"Generated {len(configs)} test cases")
